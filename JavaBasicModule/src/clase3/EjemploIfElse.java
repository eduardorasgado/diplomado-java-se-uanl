package clase3;

public class EjemploIfElse {

  public static void main(String[] args) {
    int num = 5; // variable de control
    System.out.println("Antes del if");

    if (num >= 5) {
      System.out.println("if dentro del TRUE");
    } else {
      System.out.println("if dentro del ELSE");
    }

    System.out.println("Despues del if");
  }
}
