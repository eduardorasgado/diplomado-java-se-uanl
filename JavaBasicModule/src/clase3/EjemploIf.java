public class EjemploIf {

  public static void main(String[] args) {
    int num = 5; // variable de control
    System.out.println("Antes del if");

    if (num >= 5) {
      System.out.println(num + " es mayor a o igual 5");
    }

    System.out.println("Despues del if");
  }
}
